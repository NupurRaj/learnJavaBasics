package examplePackageForTestNg;

public class Calculator {
	
	public int add1(int a, int b) {
		int sum =a + b;
		return sum;
		}

	public int sub1(int a, int b) {
		int diff =a - b;
		return diff;
		}

	public int mul1(int a, int b) {
		int mult =a * b;
		return mult;
		}

	public int div1(int a, int b) {
		int div =a / b;
		return div;
		}
}
